class Lenguaje():
    def __init__(self, nombre, anyo, descripcion):
        self.nombre = nombre
        self.anyo = anyo
        self.descripcion = descripcion
    
    def getnombre(self):
        return self.nombre
    
    def getanyo(self):
        return self.anyo
    
    def getdescripcion(self):
        return self.descripcion
        